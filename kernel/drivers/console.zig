/// Framebuffer text console with VT100 escape code support.
///
/// Uses Limine framebuffer, embedded 8x16 bitmap font, and ANSI color codes.
/// Integrated with serial output — write() goes to both serial and framebuffer.

const limine = @import("../limine.zig");
const serial = @import("../arch/x86_64/serial.zig");
const hhdm = @import("../mm/hhdm.zig");

// ---- Framebuffer state ----

var fb: [*]u8 = undefined;
var fb_width: u32 = 0;
var fb_height: u32 = 0;
var fb_pitch: u32 = 0;
var fb_bpp: u16 = 0;
var enabled: bool = false;

// ---- Text console state ----

const FONT_W: u32 = 8;
const FONT_H: u32 = 16;

var cols: u32 = 0;
var rows: u32 = 0;
var cur_col: u32 = 0;
var cur_row: u32 = 0;
var fg: u32 = 0x00CCCCCC; // Light gray (XRGB)
var bg: u32 = 0x00000000; // Black
var default_fg: u32 = 0x00CCCCCC;
var default_bg: u32 = 0x00000000;
var bold: bool = false;

// ---- VT100 escape state ----

const EscState = enum { normal, esc, csi };
var esc_state: EscState = .normal;
var csi_params: [8]u32 = [_]u32{0} ** 8;
var csi_count: u8 = 0;
var csi_priv: bool = false;

// ---- ANSI color table (standard 8 + bright 8) ----

const ansi_colors = [16]u32{
    0x00000000, // 0 black
    0x00AA0000, // 1 red
    0x0000AA00, // 2 green
    0x00AA5500, // 3 yellow/brown
    0x000000AA, // 4 blue
    0x00AA00AA, // 5 magenta
    0x0000AAAA, // 6 cyan
    0x00AAAAAA, // 7 white (light gray)
    0x00555555, // 8 bright black (dark gray)
    0x00FF5555, // 9 bright red
    0x0055FF55, // 10 bright green
    0x00FFFF55, // 11 bright yellow
    0x005555FF, // 12 bright blue
    0x00FF55FF, // 13 bright magenta
    0x0055FFFF, // 14 bright cyan
    0x00FFFFFF, // 15 bright white
};

// ---- Initialization ----

pub fn init() void {
    const resp = readVolatilePtr(&fb_request.response) orelse {
        serial.writeString("[fb]   No framebuffer from Limine\n");
        return;
    };

    if (resp.framebuffer_count == 0) {
        serial.writeString("[fb]   No framebuffers available\n");
        return;
    }

    const fbinfo = resp.framebuffers[0];
    fb = fbinfo.address;
    fb_width = @truncate(fbinfo.width);
    fb_height = @truncate(fbinfo.height);
    fb_pitch = @truncate(fbinfo.pitch);
    fb_bpp = fbinfo.bpp;

    cols = fb_width / FONT_W;
    rows = fb_height / FONT_H;

    // Clear screen to black
    var y: u32 = 0;
    while (y < fb_height) : (y += 1) {
        const row_ptr = fb + y * fb_pitch;
        var x: u32 = 0;
        while (x < fb_width) : (x += 1) {
            const px: [*]u8 = row_ptr + x * 4;
            px[0] = 0; // B
            px[1] = 0; // G
            px[2] = 0; // R
            px[3] = 0; // A
        }
    }

    enabled = true;

    // Register as serial output mirror — boot messages go to both serial and FB
    serial.console_hook = &write;

    serial.writeString("[fb]   Framebuffer console: ");
    writeDecSerial(fb_width);
    serial.writeString("x");
    writeDecSerial(fb_height);
    serial.writeString(" (");
    writeDecSerial(cols);
    serial.writeString("x");
    writeDecSerial(rows);
    serial.writeString(" text, ");
    writeDecSerial(fb_bpp);
    serial.writeString("bpp)\n");
}

// ---- Public API ----

/// Write a buffer to the framebuffer console (no serial — caller handles that).
pub fn write(buf: [*]const u8, count: usize) void {
    if (!enabled) return;
    for (0..count) |i| {
        processChar(buf[i]);
    }
}

/// Check if framebuffer console is active.
pub fn isEnabled() bool {
    return enabled;
}

// ---- Character processing with VT100 ----

fn processChar(c: u8) void {
    switch (esc_state) {
        .normal => {
            if (c == 0x1B) {
                esc_state = .esc;
            } else {
                putChar(c);
            }
        },
        .esc => {
            if (c == '[') {
                esc_state = .csi;
                csi_count = 0;
                csi_priv = false;
                for (0..8) |i| csi_params[i] = 0;
            } else {
                // Unknown escape — emit as-is
                esc_state = .normal;
                putChar(0x1B);
                putChar(c);
            }
        },
        .csi => {
            if (c == '?') {
                csi_priv = true;
            } else if (c >= '0' and c <= '9') {
                if (csi_count == 0) csi_count = 1;
                csi_params[csi_count - 1] = csi_params[csi_count - 1] * 10 + (c - '0');
            } else if (c == ';') {
                if (csi_count < 8) csi_count += 1;
            } else {
                // Final character — execute CSI command
                if (csi_count == 0) csi_count = 1;
                handleCSI(c);
                esc_state = .normal;
            }
        },
    }
}

fn handleCSI(cmd: u8) void {
    const p0 = csi_params[0];

    switch (cmd) {
        'A' => { // Cursor up
            const n = if (p0 == 0) 1 else p0;
            if (cur_row >= n) cur_row -= n else cur_row = 0;
        },
        'B' => { // Cursor down
            const n = if (p0 == 0) 1 else p0;
            cur_row += n;
            if (cur_row >= rows) cur_row = rows - 1;
        },
        'C' => { // Cursor right
            const n = if (p0 == 0) 1 else p0;
            cur_col += n;
            if (cur_col >= cols) cur_col = cols - 1;
        },
        'D' => { // Cursor left
            const n = if (p0 == 0) 1 else p0;
            if (cur_col >= n) cur_col -= n else cur_col = 0;
        },
        'H', 'f' => { // Cursor position
            const row = if (p0 == 0) 1 else p0;
            const col = if (csi_count >= 2 and csi_params[1] > 0) csi_params[1] else 1;
            cur_row = if (row - 1 < rows) row - 1 else rows - 1;
            cur_col = if (col - 1 < cols) col - 1 else cols - 1;
        },
        'J' => { // Erase in display
            if (p0 == 2 or p0 == 3) {
                // Clear entire screen
                clearScreen();
                cur_row = 0;
                cur_col = 0;
            } else if (p0 == 0) {
                // Clear from cursor to end
                clearFromCursor();
            }
        },
        'K' => { // Erase in line
            if (p0 == 0) {
                // Clear from cursor to end of line
                var c: u32 = cur_col;
                while (c < cols) : (c += 1) {
                    drawChar(cur_row, c, ' ', fg, bg);
                }
            } else if (p0 == 2) {
                // Clear entire line
                var c: u32 = 0;
                while (c < cols) : (c += 1) {
                    drawChar(cur_row, c, ' ', fg, bg);
                }
            }
        },
        'm' => { // Set Graphics Rendition
            var i: u8 = 0;
            while (i < csi_count) : (i += 1) {
                const p = csi_params[i];
                if (p == 0) {
                    fg = default_fg;
                    bg = default_bg;
                    bold = false;
                } else if (p == 1) {
                    bold = true;
                } else if (p >= 30 and p <= 37) {
                    fg = ansi_colors[p - 30 + if (bold) @as(u32, 8) else 0];
                } else if (p >= 40 and p <= 47) {
                    bg = ansi_colors[p - 40];
                } else if (p >= 90 and p <= 97) {
                    fg = ansi_colors[p - 90 + 8];
                } else if (p >= 100 and p <= 107) {
                    bg = ansi_colors[p - 100 + 8];
                } else if (p == 39) {
                    fg = default_fg;
                } else if (p == 49) {
                    bg = default_bg;
                }
            }
        },
        else => {}, // Ignore unknown CSI commands
    }
}

// ---- Text output ----

fn putChar(c: u8) void {
    switch (c) {
        '\n' => {
            cur_col = 0;
            cur_row += 1;
            if (cur_row >= rows) scroll();
        },
        '\r' => {
            cur_col = 0;
        },
        '\t' => {
            const next = (cur_col + 8) & ~@as(u32, 7);
            cur_col = if (next < cols) next else cols - 1;
        },
        0x08 => { // Backspace
            if (cur_col > 0) {
                cur_col -= 1;
                drawChar(cur_row, cur_col, ' ', fg, bg);
            }
        },
        else => {
            if (c >= 0x20 and c < 0x7F) {
                drawChar(cur_row, cur_col, c, fg, bg);
                cur_col += 1;
                if (cur_col >= cols) {
                    cur_col = 0;
                    cur_row += 1;
                    if (cur_row >= rows) scroll();
                }
            }
            // Non-printable chars below 0x20 (except handled above) are ignored
        },
    }
}

fn scroll() void {
    // Move all rows up by one (copy pixels)
    const line_height = FONT_H * fb_pitch;
    const total_area = (rows - 1) * line_height;

    // Copy rows 1..N to 0..N-1
    const src = fb + line_height;
    const dst = fb;
    var i: u32 = 0;
    while (i < total_area) : (i += 1) {
        dst[i] = src[i];
    }

    // Clear the last row
    cur_row = rows - 1;
    var c: u32 = 0;
    while (c < cols) : (c += 1) {
        drawChar(cur_row, c, ' ', fg, bg);
    }
}

fn clearScreen() void {
    var r: u32 = 0;
    while (r < rows) : (r += 1) {
        var c: u32 = 0;
        while (c < cols) : (c += 1) {
            drawChar(r, c, ' ', fg, bg);
        }
    }
}

fn clearFromCursor() void {
    // Clear rest of current line
    var c: u32 = cur_col;
    while (c < cols) : (c += 1) {
        drawChar(cur_row, c, ' ', fg, bg);
    }
    // Clear all subsequent lines
    var r: u32 = cur_row + 1;
    while (r < rows) : (r += 1) {
        c = 0;
        while (c < cols) : (c += 1) {
            drawChar(r, c, ' ', fg, bg);
        }
    }
}

// ---- Pixel-level rendering ----

fn drawChar(row: u32, col: u32, c: u8, fg_color: u32, bg_color: u32) void {
    const glyph_idx: usize = if (c >= 0x20 and c < 0x7F) c - 0x20 else 0;
    const glyph = &font[glyph_idx];

    const px_x = col * FONT_W;
    const px_y = row * FONT_H;

    var dy: u32 = 0;
    while (dy < FONT_H) : (dy += 1) {
        const row_bits = glyph[dy];
        const scanline = fb + (px_y + dy) * fb_pitch + px_x * 4;
        var dx: u32 = 0;
        while (dx < FONT_W) : (dx += 1) {
            const bit = (row_bits >> @as(u3, @truncate(7 - dx))) & 1;
            const color = if (bit != 0) fg_color else bg_color;
            const px: [*]u8 = scanline + dx * 4;
            px[0] = @truncate(color); // B
            px[1] = @truncate(color >> 8); // G
            px[2] = @truncate(color >> 16); // R
            px[3] = 0xFF; // A
        }
    }
}

// ---- Limine framebuffer request ----

pub export var fb_request: limine.FramebufferRequest linksection(".limine_reqs") = .{};

fn readVolatilePtr(ptr: *?*limine.FramebufferResponse) ?*limine.FramebufferResponse {
    const vptr: *volatile ?*limine.FramebufferResponse = @ptrCast(ptr);
    return vptr.*;
}

fn writeDecSerial(val: anytype) void {
    const v: u64 = @intCast(val);
    if (v == 0) {
        serial.writeByte('0');
        return;
    }
    var buf: [20]u8 = undefined;
    var n = v;
    var i: usize = 20;
    while (n > 0) {
        i -= 1;
        buf[i] = @truncate((n % 10) + '0');
        n /= 10;
    }
    serial.writeString(buf[i..]);
}

